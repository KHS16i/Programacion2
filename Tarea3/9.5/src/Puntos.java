public abstract class Puntos {
    private int punto;
    protected int puntos;

    public void setPunto(int punto) {
        this.punto = punto;
    }

    public int getPunto() {
        return punto;
    }

    public void setPuntos(int puntos) {
        this.puntos = puntos;
    }

    public int getPuntos() {
        return puntos;
    }
}
